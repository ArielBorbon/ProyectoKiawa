    private void btnConfirmarPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarPagarActionPerformed
        // TODO add your handling code here:
        if (this.cbxMedioPago.getSelectedItem().toString()!="Efectivo") {
            controlOrdenarPlatillo.iniciarFrmMensajePedidoFallido();
            dispose();
        }else{
            controlOrdenarPlatillo.iniciarFrmMensajePedidoExitoso();
            dispose();
        }
        
    }//GEN-LAST:event_btnConfirmarPagarActionPerformed

