    private void btnSeleccionarUbicacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarUbicacionActionPerformed
        // TODO add your handling code here:
        if (UbicacionSeleccionada == null) {
            JOptionPane.showMessageDialog(this, "No has seleccionado ninguna ubicacion.", "Error", JOptionPane.WARNING_MESSAGE);
        }else if(this.txtTelefono.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No has proporcionado un numero de telefono.", "Error", JOptionPane.WARNING_MESSAGE);
            
        }else if (!this.txtTelefono.getText().matches("[0-9]+")) {
            JOptionPane.showMessageDialog(this, "El número de teléfono solo debe contener números.", "Error", JOptionPane.WARNING_MESSAGE);
            
        }else if (this.txtTelefono.getText().length() != 10) {
            JOptionPane.showMessageDialog(this, "El número de teléfono debe tener 10 dígitos.", "Error", JOptionPane.WARNING_MESSAGE);
                            
        }else{
            controlOrdenarPlatillo.iniciarFrmConfirmarPedido();
            dispose();
        }
        
    }//GEN-LAST:event_btnSeleccionarUbicacionActionPerformed

