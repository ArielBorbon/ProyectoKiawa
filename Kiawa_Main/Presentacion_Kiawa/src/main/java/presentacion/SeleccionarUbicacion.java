    /*Llenado de la tabla*/
    private void llenarTablaUbicaciones(){
        DefaultTableModel modelo = (DefaultTableModel)this.tblUbicaciones.getModel();
        modelo.setRowCount(0);
        for(UbicacionDTO ubicacion : this.listaUbicaciones){
            Object[] filaTabla = {
                ubicacion.getEdificio(),
                ubicacion.getAula(),
            };
            modelo.addRow(filaTabla);
        }
    }
    
    
    /*Este metodo guarda la informacion de la fila seleccionada y los datos de los campos de instrucciones y telefono*/
    private void guardarUbicacion(){
        
        int filaSeleccionada = this.tblUbicaciones.getSelectedRow();
        if (filaSeleccionada!=-1 && !this.txtTelefono.getText().isEmpty()) {
            /*Se recuperan los varoles del campo de la tabla seleccionada*/
            String edificio = this.tblUbicaciones.getValueAt(filaSeleccionada, 0).toString();
            String aula = this.tblUbicaciones.getValueAt(filaSeleccionada, 1).toString();
            /*Se recuperan los varoles de los campos de texto*/
            String telefono = this.txtTelefono.getText();
            String instrucciones = this.txtInstrucciones.getText();

            /*Se añade a la lista la ubicacion seleccionada*/
            this.UbicacionSeleccionada = new UbicacionDTO(edificio,aula,telefono,instrucciones);

            /*Se muestra la ubicacion en consola era para pruebas*/
            System.out.println("Ubicacion e instrucciones seleccionadas:");

            
            System.out.println(UbicacionSeleccionada.getEdificio() + " " + UbicacionSeleccionada.getAula() + " " + UbicacionSeleccionada.getTelefono() + " " + UbicacionSeleccionada.getInstruccionesEntrega());
            
        }
    }
    
    /*Este metodo solo selecciona una fila de la tabla pero no guarda nada*/
    private void seleccionarUbicacion(){
        this.tblUbicaciones.getSelectionModel().addListSelectionListener(e->{
            if (!e.getValueIsAdjusting()) {
                int filaSeleccionada = this.tblUbicaciones.getSelectedRow();
                if (filaSeleccionada!=-1) {
                    System.out.println("fila seleccionada: "+ filaSeleccionada);
                    
                }
            }
        });
    }
    
