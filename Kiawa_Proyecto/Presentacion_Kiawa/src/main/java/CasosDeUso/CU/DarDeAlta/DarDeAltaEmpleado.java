    private void btnCrearEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearEmpleadoActionPerformed
        try {

            String rol = cmbRol.getSelectedItem().toString();
            String nombre = txtNombre.getText().trim();
            String telefono = txtTelefono.getText().trim();
            String domicilio = txtDomicilio.getText().trim();
            String apodo = txtApodo.getText().trim();
            String salarioStr = (txtSalarioDiario.getText().trim());
            String dias = txtDiasDeTrabajo.getText().trim();
            String horario = txtHorario.getText().trim();
            String extras = txtConsideracionesExtras.getText().trim();
            String curp = txtCurp.getText().trim();
            
            
                    double salario;
        try {
            salario = Double.parseDouble(salarioStr);
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this,
                    "Salario diario inv√°lido, ingresa un n√∫mero.",
                    "Error de formato", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        if (!telefono.matches("\\d{10}")) {
            JOptionPane.showMessageDialog(this,
                    "El tel√©fono debe contener exactamente 10 d√≠gitos.",
                    "Error de formato", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
            
            if (salario <= 0) {
                JOptionPane.showMessageDialog(this, "El salario tiene que ser mayor a 0" , "Error" , JOptionPane.WARNING_MESSAGE);
            }

            if (nombre.isEmpty() || telefono.isEmpty() || domicilio.isEmpty() || apodo.isEmpty() || dias.isEmpty() || horario.isEmpty() || extras.isEmpty() || curp.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Por favor, rellena todos los campos.",
                        "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
                return;
            }

            FSubsistema_Administrador fSubsistemaAdmin = new FSubsistema_Administrador();
            if (fSubsistemaAdmin.existeEmpleadoConCurp(txtCurp.getText())) {
                JOptionPane.showMessageDialog(this, "Ya existe un empleado con esa CURP", "Duplicacion", JOptionPane.WARNING_MESSAGE);
                return;
            }

            if ("COCINERO".equalsIgnoreCase(rol)) {

                FSubsistema_Cocinero subsistema = new FSubsistema_Cocinero();

                String nuevoId = subsistema.generarNuevoIDCocinero();

                CocineroDTO dto = new CocineroDTO();
                dto.setIdCocinero(nuevoId);
                dto.setNombreCompleto(nombre);
                dto.setTelefono(telefono);
                dto.setDomicilio(domicilio);
                dto.setApodo(apodo);
                dto.setSalarioDiario(salario);
                dto.setDiasTrabajo(dias);
                dto.setHorario(horario);
                dto.setConsideracionesExtras(extras);
                dto.setCurp(curp);
                dto.setDisponible(true);
                String contrasena = txtContrasena.getText().trim();

                boolean ok = subsistema.registrarCocinero(dto, contrasena);
                if (ok) {
                    JOptionPane.showMessageDialog(this,
                            "Cocinero creado con ID " + nuevoId,
                            "√âxito", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this,
                            "No se pudo crear el cocinero (ya existe?).",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }

            } else {
                // 3b) Crear Repartidor
                FSubsistema_Repartidor subsistema = new FSubsistema_Repartidor();
                String nuevoId = subsistema.crearIDFriendlyRepartidor();
                RepartidorDTO dto = new RepartidorDTO();
                dto.setIdRepartidor(nuevoId);
                dto.setNombreCompleto(nombre);
                dto.setTelefono(telefono);
                dto.setDomicilio(domicilio);
                dto.setApodo(apodo);
                dto.setSalarioDiario(salario);
                dto.setDiasTrabajo(dias);
                dto.setHorario(horario);
                dto.setConsideracionesExtras(extras);
                dto.setCurp(curp);
                dto.setDisponible(true);

                boolean ok = subsistema.crearRepartidor(dto);
                if (ok) {
                    JOptionPane.showMessageDialog(this,
                            "Repartidor creado con ID " + nuevoId,
                            "√âxito", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this,
                            "No se pudo crear el repartidor (ya existe?).",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            }

        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this,
                    "Salario diario inv√°lido, ingresa un n√∫mero.",
                    "Error de formato", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Error al crear el empleado:\n" + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCrearEmpleadoActionPerformed

