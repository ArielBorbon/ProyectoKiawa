        /** Llama a este método tras crear la ventana para rellenar los campos */
    public void acomodarCampos() {
        Object sel = ControlAdministrador.getInstancia().getEmpleadoSeleccionado();
        if (sel instanceof RepartidorDTO) {
            RepartidorDTO dto = (RepartidorDTO) sel;
            lblId.setText(dto.getIdRepartidor());
            txtNombre.setText(dto.getNombreCompleto());
            lblCurp.setText(dto.getCurp());
            txtSalarioDiario.setText(dto.getSalarioDiario()+"");
            txtTelefono.setText(dto.getTelefono());
            txtApodo.setText(dto.getApodo());
            txtDomicilio.setText(dto.getDomicilio());
            txtHorario.setText(dto.getHorario());
            txtDiasDeTrabajo.setText(dto.getDiasTrabajo());
            txtConsideracionesExtras.setText(dto.getConsideracionesExtras());
            cmbDisponible.setSelectedItem(dto.getDisponible());
        } else if (sel instanceof CocineroDTO) {
            CocineroDTO dto = (CocineroDTO) sel;
            lblId.setText(dto.getIdCocinero());
            txtNombre.setText(dto.getNombreCompleto());
            lblCurp.setText(dto.getCurp());
            txtSalarioDiario.setText(dto.getSalarioDiario()+"");
            txtTelefono.setText(dto.getTelefono());
            txtApodo.setText(dto.getApodo());
            txtDomicilio.setText(dto.getDomicilio());
            txtHorario.setText(dto.getHorario());
            txtDiasDeTrabajo.setText(dto.getDiasTrabajo());
            txtConsideracionesExtras.setText(dto.getConsideracionesExtras());
            cmbDisponible.setSelectedItem(dto.getDisponible());
        }
    }

