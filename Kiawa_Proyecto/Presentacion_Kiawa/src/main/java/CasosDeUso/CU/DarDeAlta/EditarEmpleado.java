    private void btnModificarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarEmpleadoActionPerformed

        String nombre = txtNombre.getText().trim();
        String telefono = txtTelefono.getText().trim();
        String domicilio = txtDomicilio.getText().trim();
        String apodo = txtApodo.getText().trim();
        String salarioStr = txtSalarioDiario.getText().trim();
        String dias = txtDiasDeTrabajo.getText().trim();
        String horario = txtHorario.getText().trim();
        String extras = txtConsideracionesExtras.getText().trim();
        String curp = lblCurp.getText().trim();
        String contrasena = new String(txtContrasena.getPassword()).trim();
        
        if (contrasena == "") {
            
        }

        if (nombre.isBlank() || telefono.isBlank() || domicilio.isBlank() || apodo.isBlank()
                || salarioStr.isBlank() || dias.isBlank() || horario.isBlank() || curp.isBlank()) {
            JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos obligatorios.", "Campos vacíos", JOptionPane.WARNING_MESSAGE);
            return;
        }

        double salario;
        try {
            salario = Double.parseDouble(salarioStr);
            if (salario <= 0) {
                throw new NumberFormatException();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "El salario debe ser un número válido y mayor a 0.", "Salario inválido", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Object obj = ControlAdministrador.getInstancia().getEmpleadoSeleccionado();

        try {
            if (obj instanceof RepartidorDTO dto) {
                dto.setNombreCompleto(nombre);
                dto.setTelefono(telefono);
                dto.setDomicilio(domicilio);
                dto.setApodo(apodo);
                dto.setSalarioDiario(salario);
                dto.setDiasTrabajo(dias);
                dto.setHorario(horario);
                dto.setConsideracionesExtras(extras);
                dto.setCurp(curp);
              

                FSubsistema_Repartidor subs = new FSubsistema_Repartidor();
                subs.actualizarRepartidor(dto, contrasena); 
            } else if (obj instanceof CocineroDTO dto) {
                dto.setNombreCompleto(nombre);
                dto.setTelefono(telefono);
                dto.setDomicilio(domicilio);
                dto.setApodo(apodo);
                dto.setSalarioDiario(salario);
                dto.setDiasTrabajo(dias);
                dto.setHorario(horario);
                dto.setConsideracionesExtras(extras);
                dto.setCurp(curp);

                FSubsistema_Cocinero subs = new FSubsistema_Cocinero();
                subs.actualizarCocinero(dto, contrasena);
            }

            JOptionPane.showMessageDialog(this, "Empleado modificado correctamente.");
        this.dispose();
     //   ControlAdministrador.getInstancia().regresar();
            
            new GestionarEmpleados().setVisible(true);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al modificar: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

        txtApodo.setText("");
        txtConsideracionesExtras.setText("");
        txtContrasena.setText("");
        lblCurp.setText("");
        txtDiasDeTrabajo.setText("");
        txtDomicilio.setText("");
        txtHorario.setText("");
        txtNombre.setText("");
        txtSalarioDiario.setText("");
        txtTelefono.setText("");
    


    }//GEN-LAST:event_btnModificarEmpleadoActionPerformed

