private void llenarTablaUbicaciones(String filtroEdificio) {
    try {
        FSubsistema_Ubicaciones fUbicaciones = new FSubsistema_Ubicaciones();
        List<UbicacionDTO> lista;
        if (filtroEdificio == null || filtroEdificio.trim().isEmpty()) {
            lista = fUbicaciones.obtenerListaUbicaciones();
        } else {
            lista = fUbicaciones.filtrarPorEdificio(filtroEdificio.trim());
        }
        NonEditableTableModel modelo = new NonEditableTableModel();
        modelo.addColumn("Edificio");
        modelo.addColumn("Sal√≥n");
        for (UbicacionDTO ubicacion : lista) {
            modelo.addRow(new Object[]{ubicacion.getEdificio(), ubicacion.getSalon()});
        }
        tblUbicaciones.setModel(modelo);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al llenar la tabla: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
